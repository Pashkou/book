package domain.payment.classification;

import java.util.Date;

public class TimeCard {
	private Date date;
	private double hours;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public double getHours() {
		return hours;
	}
	public void setHours(double hours) {
		this.hours = hours;
	}
	
	
	
}
